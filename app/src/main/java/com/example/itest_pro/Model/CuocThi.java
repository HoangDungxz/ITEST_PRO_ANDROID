package com.example.itest_pro.Model;

import java.util.Date;

public class CuocThi {

    private Integer id;

    private String ngaythi;

    private int giothi;

    private int phutthi;

    private String monthi;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNgaythi() {
        return ngaythi;
    }

    public void setNgaythi(String ngaythi) {
        this.ngaythi = ngaythi;
    }

    public int getGiothi() {
        return giothi;
    }

    public void setGiothi(int giothi) {
        this.giothi = giothi;
    }

    public String getMonthi() {
        return monthi;
    }

    public void setMonthi(String monthi) {
        this.monthi = monthi;
    }

    public int getPhutthi() {
        return phutthi;
    }

    public void setPhutthi(int phutthi) {
        this.phutthi = phutthi;
    }

}
